"""Математический агент с двумя состояниями.

Демонстрирует работу с переходами между состояниями:
- Рабочее состояние с инструментами
- Состояние подведения итогов
- Условный переход по ключевому слову
"""

from agent_engine import AgentConfig, State, Transition, Conditions


class MyAgent(AgentConfig):
    """Математический агент для вычислений и подведения итогов.
    
    Граф: [work] ⟳ → [summarize] → END
    
    Состояния:
    - work: Основная работа с циклом (пока не скажет "ЗАДАЧА_РЕШЕНА")
    - summarize: Подведение итогов
    
    Используется для демонстрации:
    - Циклические состояния (work возвращается в себя)
    - Условные переходы по ключевому слову
    - Работа с памятью между состояниями
    """
    
    entry_point = "work"
    
    states = [
        State(
            name="work",
            tools=[
                "calculator",    # Математические вычисления
                "ask_human",     # Уточняющие вопросы пользователю
                "think",         # Внутренние размышления
                "memory"         # Сохранение/чтение заметок
            ],
            prompt="""Ты полезный помощник для математических вычислений.

Твои возможности:
- calculator: вычисление математических выражений
- ask_human: задать вопрос пользователю, если не хватает данных
- think: зафиксировать свои размышления
- memory: сохранить промежуточные результаты или прочитать сохраненное

Алгоритм работы:
1. Проанализируй запрос пользователя
2. Если не хватает данных - используй ask_human для уточнения
3. Используй think для размышлений о подходе к решению
4. Выполни вычисления через calculator
5. Сохрани важные результаты через memory
6. Когда задача полностью решена, обязательно скажи "ЗАДАЧА_РЕШЕНА"

Важно: Говори "ЗАДАЧА_РЕШЕНА" только когда задача действительно выполнена полностью!
""",
            description="Основное рабочее состояние с полным набором инструментов"
        ),
        
        State(
            name="summarize",
            tools=[
                "summarize",     # Создание саммари
                "memory"         # Доступ к сохраненным данным
            ],
            prompt="""Ты ассистент для подведения итогов выполненной работы.

Твои возможности:
- summarize: создать структурированное саммари
- memory: прочитать сохраненные данные

Твоя задача:
1. Проанализируй всю историю диалога
2. Используй рефлексию:
   - Что было сделано?
   - Какие инструменты использовались?
   - Какие промежуточные шаги были выполнены?
   - Какой получен финальный результат?
3. Создай краткое и понятное саммари через инструмент summarize
4. Сохрани саммари в память для истории

Стиль: профессиональный, структурированный, с акцентом на результаты.
""",
            description="Финальное состояние для рефлексии и подведения итогов"
        )
    ]
    
    transitions = [
        # Переход 1: Work → Summarize (когда задача решена)
        # Агент работает циклически в состоянии Work, пока не скажет "ЗАДАЧА_РЕШЕНА"
        Transition(
            from_state="work",
            to_state="summarize",
            condition=Conditions.contains_keyword("ЗАДАЧА_РЕШЕНА", case_sensitive=False),
            description=(
                "Переход из рабочего состояния в состояние подведения итогов. "
                "Срабатывает, когда агент говорит 'ЗАДАЧА_РЕШЕНА'. "
                "Если условие не выполнено, агент остается в Work и продолжает работу."
            )
        ),
        
        # Переход 2: Summarize → END (завершение)
        Transition(
            from_state="summarize",
            to_state="END",
            condition=Conditions.always_true,
            description=(
                "Безусловное завершение работы после подведения итогов. "
                "Состояние Summarize всегда переходит в END."
            )
        )
    ]
